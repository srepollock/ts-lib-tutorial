/**
 * Adds two numbers together
 * @param  {number} x
 * @param  {number} y
 * @returns number
 */
export function add(x: number, y: number) : number {
    return x + y;
}
/**
 * Subtracts Y from X.
 * @param  {number} x
 * @param  {number} y
 * @returns number
 */
export function subtract(x: number, y: number) : number {
    return x - y;
}
/**
 * Squares a number.
 * @param  {number} x
 * @returns number
 */
export function square(x: number) : number {
    return x ** 2;
}
/**
 * Cubes a number
 * @param  {number} x
 * @returns number
 */
export function cube(x: number) : number {
    return x ** 3;
}